function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, 'utf8'))
}

function atomicWriteJson(filePath, value) {
  const temporaryPath = `${filePath}.${process.pid}.tmp`
  try {
    fs.writeFileSync(temporaryPath, JSON.stringify(value, null, 2))
    fs.renameSync(temporaryPath, filePath)
  } catch (error) {
    try { fs.unlinkSync(temporaryPath) } catch {}
    throw error
  }
}

if (!fs.existsSync(curatedPath)) throw new Error('curated_icons.json not found; run build-curated-list.js first')
const curated = readJson(curatedPath)
if (!curated || !Array.isArray(curated.icons)) throw new Error('curated_icons.json must contain an icons array')
const curatedByName = new Map(curated.icons.map(icon => [icon.name, icon]))
const existingDictionary = fs.existsSync(outputPath) ? readJson(outputPath) : []
const sourceDictionaries = [existingDictionary]
const existingById = new Map()
const existingByConcept = new Map()
for (const source of sourceDictionaries) {
  if (!Array.isArray(source)) continue
  for (const icon of source) {
    if (!icon || typeof icon.id !== 'string') continue
    const existing = existingById.get(icon.id)
    const merged = {
      id: icon.id,
      name: existing?.name || icon.name,
      keywords: [...new Set([...(existing?.keywords || []), ...(icon.keywords || [])])],
    }
    existingById.set(icon.id, merged)
    if (typeof merged.name === 'string' && merged.name) {
      const concept = existingByConcept.get(merged.name)
      existingByConcept.set(merged.name, {
        id: merged.id,
        name: merged.name,
        keywords: [...new Set([...(concept?.keywords || []), ...merged.keywords])],
      })
    }
  }
}

const dictionary = fs.readdirSync(iconsDir)
  .filter(file => file.endsWith('.svg'))
  .sort()
  .map(file => {
    if (!iconIdPattern.test(file)) throw new Error(`invalid icon filename: ${file}`)
    const curatedIcon = curatedByName.get(file)
    const curatedConcept = typeof curatedIcon?.concept === 'string' ? curatedIcon.concept.toLowerCase() : undefined
    const existingIcon = existingById.get(file) || (curatedConcept ? existingByConcept.get(curatedConcept) : undefined)
    const concept = String(curatedIcon?.concept || existingIcon?.name || file.replace(/\.svg$/, '').replace(/^[^_]+_/, '')).toLowerCase()
    const tokens = concept.split(/[_-]/).filter(token => token.length > 1)
    const keywords = new Set([concept, ...tokens, ...(existingIcon?.keywords || [])])
    for (const [key, aliases] of Object.entries(semanticMap)) {
      if (concept.includes(key) || tokens.includes(key)) aliases.forEach(alias => keywords.add(alias))
    }
    for (const alias of supplementalConceptAliases[concept] || []) keywords.add(alias)

    if (tokens.some(token => ['bug', 'beetle', 'lady', 'caterpillar', 'ant', 'spider', 'cockroach', 'mosquito'].includes(token))) {
      for (const alias of [...semanticMap.bug, ...semanticMap.debug, ...semanticMap.beetle]) keywords.add(alias)
    }
    if (tokens.some(token => ['wall', 'brick'].includes(token))) {
      for (const alias of [...semanticMap.wall, ...semanticMap.brick]) keywords.add(alias)
    }
    if (tokens.some(token => ['money', 'coin', 'banknote', 'dollar', 'euro', 'yen', 'pound', 'credit', 'ledger'].includes(token))) {
      for (const alias of [...semanticMap.money, '财务', '记账']) keywords.add(alias)
    }

    const chineseKeywordCount = [...keywords].filter(keyword => chineseKeywordPattern.test(keyword)).length
    if (chineseKeywordCount < 5) {
      throw new Error(`${file} (${concept}) has only ${chineseKeywordCount} Chinese search keywords`)
    }
    return { id: file, name: concept, keywords: [...keywords] }
  })

