const finalDictionary = [];

iconList.forEach(icon => {
    // Check if physically exists to prevent rendering broken icons
    const iconFilePath = path.join(customIconsDir, icon.name);
    if (!fs.existsSync(iconFilePath)) {
        return; // Skip 404s or failed downloads
    }

    let rawKeywords = [icon.concept];

    // Tokenize concept to ensure perfect decoupling matches
    // e.g. "pile_of_poo" -> ["pile", "of", "poo"]
    // "status_lady_beetle" -> ["status", "lady", "beetle"]
    let conceptTokens = icon.concept.toLowerCase().split(/[_\-]/);

    // Find semantic matches by looking at EACH individual token AND the full concept string
    for (const key in semanticMap) {
        if (icon.concept.toLowerCase().includes(key) || conceptTokens.includes(key)) {
            rawKeywords = rawKeywords.concat(semanticMap[key]);
        }
    }

    // Custom deep associations to guarantee 100% decoupling coverage
    // If it is any bug/insect, map to ALL bug/debug terms
    if (conceptTokens.some(t => ['bug', 'beetle', 'lady', 'caterpillar', 'ant', 'spider', 'cockroach', 'mosquito'].includes(t))) {
         rawKeywords = rawKeywords.concat(semanticMap['bug'], semanticMap['debug'], semanticMap['beetle']);
    }

    // If it is a wall or brick, map both
    if (conceptTokens.some(t => ['wall', 'brick'].includes(t))) {
         rawKeywords = rawKeywords.concat(semanticMap['wall'], semanticMap['brick']);
    }

    // If it is money related
    if (conceptTokens.some(t => ['money', 'coin', 'banknote', 'dollar', 'euro', 'yen', 'pound', 'credit', 'ledger'].includes(t))) {
         rawKeywords = rawKeywords.concat(semanticMap['money'], ['财务', '记账']);
    }

    // Split snake case and hyphens as root keywords
    conceptTokens.forEach(word => {
        if (word && word.length > 1) rawKeywords.push(word);
    });

    // Unique
    let uniqueKeywords = [...new Set(rawKeywords)];
    let finalKeywordsSet = new Set(uniqueKeywords);

    finalDictionary.push({
        id: icon.name,
        name: icon.concept,
        keywords: Array.from(finalKeywordsSet)
    });
});

fs.writeFileSync(outputDict, JSON.stringify(finalDictionary, null, 2));
console.log(`Generated BULLETPROOF icon dictionary without pinyin for ${finalDictionary.length} variants at ${outputDict}`);
